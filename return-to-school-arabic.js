/* =========================================================================
   Translation engine
   Walks every text node in the page (and translatable attributes) and
   swaps EN ↔ AR using the dictionary above.
   ========================================================================= */

(function() {
    const SKIP_TAGS = new Set(['SCRIPT','STYLE','NOSCRIPT','CODE','PRE','KBD']);
    const ATTRS = ['title','alt','placeholder','aria-label'];

    // Cache all translatable text nodes + their original English text.
    let cache = null;

    function collectTextNodes() {
        const walker = document.createTreeWalker(
            document.body, NodeFilter.SHOW_TEXT,
            {
                acceptNode(node) {
                    if (!node.parentElement) return NodeFilter.FILTER_REJECT;
                    if (SKIP_TAGS.has(node.parentElement.tagName))
                        return NodeFilter.FILTER_REJECT;
                    // Don't touch text inside elements marked as untranslatable
                    if (node.parentElement.closest('[data-no-translate]'))
                        return NodeFilter.FILTER_REJECT;
                    const txt = node.nodeValue;
                    if (!txt || !txt.trim()) return NodeFilter.FILTER_REJECT;
                    return NodeFilter.FILTER_ACCEPT;
                }
            }
        );
        const nodes = [];
        let n;
        while ((n = walker.nextNode())) {
            nodes.push({ node: n, en: n.nodeValue });
        }
        return nodes;
    }

    function collectAttributes() {
        const items = [];
        document.querySelectorAll('*').forEach(el => {
            if (SKIP_TAGS.has(el.tagName)) return;
            for (const a of ATTRS) {
                if (el.hasAttribute(a)) {
                    const v = el.getAttribute(a);
                    if (v && v.trim()) {
                        items.push({ el, attr: a, en: v });
                    }
                }
            }
        });
        return items;
    }

    function buildCache() {
        cache = {
            textNodes: collectTextNodes(),
            attrs: collectAttributes()
        };
    }

    function translatePiece(en) {
        if (!en) return en;
        // Exact hit
        const trimmed = en.trim();
        if (window.AR_DICT[trimmed] !== undefined) {
            // Preserve leading/trailing whitespace
            const lead = en.match(/^\s*/)[0];
            const trail = en.match(/\s*$/)[0];
            return lead + window.AR_DICT[trimmed] + trail;
        }
        return en; // Fallback: keep English if not in dictionary
    }

    function applyLanguage(lang) {
        if (!cache) buildCache();

        cache.textNodes.forEach(item => {
            item.node.nodeValue = (lang === 'ar')
                ? translatePiece(item.en)
                : item.en;
        });
        cache.attrs.forEach(item => {
            item.el.setAttribute(item.attr,
                (lang === 'ar') ? translatePiece(item.en) : item.en);
        });
    }

    // Public API
    window.AR_APPLY = applyLanguage;
    window.AR_REBUILD_CACHE = buildCache;
})();
